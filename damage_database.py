def stat():
    levels = Counter(d["严重等级"] for d in DAMAGE_DB)
    parts = Counter(d["部位"] for d in DAMAGE_DB)

    print("=" * 40)
    print("  数据库统计")
    print("=" * 40)
    print(f"\n  总记录数: {len(DAMAGE_DB)}")
    print(f"\n  按等级:")
    for lv, cnt in levels.most_common():
        print(f"    {lv}: {cnt}条")
    print(f"\n  按部位:")
    for pt, cnt in parts.most_common():
        print(f"    {pt}: {cnt}条")

    all_params = set()
    for d in DAMAGE_DB:
        for p in d["关联参数"]:
            all_params.add(p["参数名"])
    print(f"\n  关联 {len(all_params)} 个独立参数:")
    for p in sorted(all_params):
        print(f"    · {p}")


def search(keyword):
    kw = keyword.lower()
    results = []
    for d in DAMAGE_DB:
        if kw in d["损伤名称"].lower() or kw in d["部位"].lower():
            results.append(d)
            continue
        for p in d["关联参数"]:
            if kw in p["参数名"].lower():
                results.append(d)
                break
    if not results:
        print(f"[!] 未找到「{keyword}」的记录")
        return
    print(f"找到 {len(results)} 条结果:")
    for d in results:
        tag = {"轻度": "", "中度": "", "重度": ""}.get(d["严重等级"], "")
        print(f"  {tag} [{d['id']}] {d['损伤名称']}  ({d['部位']})")


