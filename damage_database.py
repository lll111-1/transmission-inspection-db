def stat():
    """统计数据库"""
    from collections import Counter
    levels = Counter(d["严重等级"] for d in DAMAGE_DB)
    parts = Counter(d["部位"] for d in DAMAGE_DB)

    print("=" * 40)
    print("  数据库统计")
    print("=" * 40)
    print(f"\n  总记录数: {len(DAMAGE_DB)}")
    print(f"\n  按等级:")
    for lv, cnt in levels.most_common():
        print(f"    {lv}: {cnt}条")
    print(f"\n  按部位:")
    for pt, cnt in parts.most_common():
        print(f"    {pt}: {cnt}条")
    print(f"\n  关联参数总数:")
    all_params = set()
    for d in DAMAGE_DB:
        for p in d["关联参数"]:
            all_params.add(p["参数名"])
    print(f"    {len(all_params)} 个独立参数")
    for p in sorted(all_params):
        print(f"    · {p}")


def risk_assessment(tower_params: dict):
    """
    给定塔参数，评估可能的损伤风险
    输入：tower_params 字典（同 TOWER 结构）
    输出：高风险/中风险/低风险损伤列表
    """
    print("=" * 60)
    print(f"  塔型风险评估: {tower_params.get('塔型', '自定义塔')}")
    print("=" * 60)

    high_risk = []
    mid_risk = []
    low_risk = []

    for d in DAMAGE_DB:
        risk_score = 0
        reasons = []
        for p in d["关联参数"]:
            param_name = p["参数名"]
            if param_name not in tower_params:
                continue
            val = tower_params[param_name]

            # 根据参数值判断风险
            if param_name == "主材截面半径":
                if val < 25:
                    risk_score += 3
                    reasons.append(f"主材偏细(r={val}mm<25mm)")
                elif val < 30:
                    risk_score += 1
            elif param_name == "横担伸出":
                if val > 8:
                    risk_score += 2
                    reasons.append(f"横担较长(伸出{val}m>8m)")
            elif param_name == "呼高":
                if val > 30:
                    risk_score += 2
                    reasons.append(f"呼高高({val}m>30m)")
            elif param_name == "绝缘子片数":
                if val > 12:
                    risk_score += 1
            elif param_name == "斜材半径":
                if val < 12:
                    risk_score += 3
            elif param_name == "地线支架高":
                if val > 4:
                    risk_score += 1
            elif param_name == "启用X形斜材":
                if not val:
                    risk_score += 1  # 无X形斜材→单根断裂影响大

        if risk_score >= 3:
            high_risk.append((d, reasons))
        elif risk_score >= 1:
            mid_risk.append((d, reasons))
        else:
            low_risk.append((d, reasons))

    print(f"\n  🔴 高风险 ({len(high_risk)}项):")
    for d, reasons in high_risk:
        print(f"    [{d['id']}] {d['损伤名称']}")
        for r in reasons:
            print(f"      ⚠ {r}")

    print(f"\n  🟡 中风险 ({len(mid_risk)}项):")
    for d, reasons in mid_risk:
        print(f"    [{d['id']}] {d['损伤名称']}")

    print(f"\n  🟢 低风险 ({len(low_risk)}项)")

    return high_risk, mid_risk, low_risk


def export_csv(filename="damage_db_export.csv"):
    """导出数据库为CSV"""
    rows = []
    for d in DAMAGE_DB:
        params_str = "; ".join([
            f"{p['参数名']}: {p['关系']}"
            for p in d["关联参数"]
        ])
        rows.append({
            "编号": d["id"],
            "损伤名称": d["损伤名称"],
            "部位": d["部位"],
            "严重等级": d["严重等级"],
            "检测方式": d["检测方式"],
            "处理方案": d["处理方案"],
            "关联参数": params_str,
            "阈值说明": d.get("阈值说明", ""),
        })

    filepath = Path(filename).resolve()
    with open(filepath, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.DictWriter(f, fieldnames=rows[0].keys())
        writer.writeheader()
        writer.writerows(rows)
    print(f"[OK] 已导出: {filepath}")


